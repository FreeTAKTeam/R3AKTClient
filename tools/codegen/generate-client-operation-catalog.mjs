fs.mkdirSync(generatedDir, { recursive: true });

fs.writeFileSync(
  path.join(generatedDir, "client-operations.json"),
  JSON.stringify(
    {
      generated_at: new Date().toISOString(),
      source: "API/ReticulumCommunityHub-SouthboundCommands.json",
      operation_count: operationCount,
      group_order: groupOrder,
      groups,
      operations,
      alias_count: aliasEntries.length,
      query_operation_count: queryOperations.length,
      command_operation_count: commandOperations.length,
    },
    null,
    2,
  ) + "\n",
);

