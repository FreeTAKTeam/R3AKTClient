import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const sourcePath = path.join(repoRoot, "docs", "R3AKTClient", "APIANnalysis_clientImplementationSet.md");
const content = fs.readFileSync(sourcePath, "utf8");

const lines = content.split(/\r?\n/);

const groups = [];
let currentGroup = "Ungrouped";

for (const line of lines) {
  const groupMatch = /^##\s+\d+\.\s+(.+)$/.exec(line.trim());
  if (groupMatch) {
    currentGroup = groupMatch[1].trim();
    continue;
  }

  const opMatch = /^\|\s*`([^`]+)`\s*\|\s*([^|]+)\|/.exec(line);
  if (!opMatch) {
    continue;
  }

  const operation = opMatch[1].trim();
  const description = opMatch[2].trim();
  if (!/^(GET|POST|PUT|PATCH|DELETE)\s+\//.test(operation)) {
    continue;
  }

  let group = groups.find((entry) => entry.name === currentGroup);
  if (!group) {
    group = { name: currentGroup, operations: [] };
    groups.push(group);
  }
  group.operations.push({ operation, description });
}

const operations = groups.flatMap((group) =>
  group.operations.map((entry) => ({
    operation: entry.operation,
    description: entry.description,
    group: group.name,
  })),
);

const unique = new Map();
for (const item of operations) {
  unique.set(item.operation, item);
}
const uniqueOperations = [...unique.values()].sort((a, b) => a.operation.localeCompare(b.operation));

if (uniqueOperations.length !== 104) {
  throw new Error(`Expected 104 client operations, found ${uniqueOperations.length}`);
}

const generatedDir = path.join(repoRoot, "docs", "R3AKTClient", "generated");
fs.mkdirSync(generatedDir, { recursive: true });

const jsonOutPath = path.join(generatedDir, "client-operations.json");
fs.writeFileSync(
  jsonOutPath,
  JSON.stringify(
    {
      generated_at: new Date().toISOString(),
      source: "docs/R3AKTClient/APIANnalysis_clientImplementationSet.md",
      operation_count: uniqueOperations.length,
      groups,
      operations: uniqueOperations,
    },
    null,
    2,
  ) + "\n",
);

const tsOutDir = path.join(repoRoot, "packages", "node-client", "src", "generated");
fs.mkdirSync(tsOutDir, { recursive: true });

const tsLines = [];
tsLines.push("// Auto-generated by tools/codegen/generate-client-operation-catalog.mjs");
tsLines.push("/* eslint-disable */");
tsLines.push("");
tsLines.push("export type ClientFeatureGroup =");
for (const [index, group] of groups.entries()) {
  const line = `  | ${JSON.stringify(group.name)}${index === groups.length - 1 ? ";" : ""}`;
  tsLines.push(line);
}
tsLines.push("");
tsLines.push("export const CLIENT_OPERATION_CATALOG = [");
for (const item of uniqueOperations) {
  tsLines.push(
    `  { operation: ${JSON.stringify(item.operation)}, group: ${JSON.stringify(item.group)}, description: ${JSON.stringify(item.description)} },`,
  );
}
tsLines.push("] as const;");
tsLines.push("");
tsLines.push("export type ClientOperation = (typeof CLIENT_OPERATION_CATALOG)[number][\"operation\"]; ");
tsLines.push("export type ClientOperationEntry = (typeof CLIENT_OPERATION_CATALOG)[number];");
tsLines.push("export const CLIENT_OPERATION_KEYS = CLIENT_OPERATION_CATALOG.map((entry) => entry.operation) as readonly ClientOperation[];");
tsLines.push("");

fs.writeFileSync(path.join(tsOutDir, "clientOperations.ts"), tsLines.join("\n") + "\n");

const rustOutDir = path.join(repoRoot, "crates", "reticulum_mobile", "src", "generated");
fs.mkdirSync(rustOutDir, { recursive: true });
const rustLines = [];
rustLines.push("// Auto-generated by tools/codegen/generate-client-operation-catalog.mjs");
rustLines.push("");
rustLines.push("pub const CLIENT_OPERATION_KEYS: &[&str] = &[");
for (const item of uniqueOperations) {
  rustLines.push(`    ${JSON.stringify(item.operation)},`);
}
rustLines.push("];\n");

fs.writeFileSync(path.join(rustOutDir, "client_operations.rs"), rustLines.join("\n"));

console.log(`Generated client operation catalog (${uniqueOperations.length} operations).`);
