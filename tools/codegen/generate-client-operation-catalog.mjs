import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const sourcePath = path.join(
  repoRoot,
  "API",
  "ReticulumCommunityHub-SouthboundCommands.json",
);
const catalog = JSON.parse(fs.readFileSync(sourcePath, "utf8"));

const groupOrder = Array.isArray(catalog.group_order) ? catalog.group_order : [];
const operations = Array.isArray(catalog.operations) ? catalog.operations : [];
const groups = groupOrder.map((name) => ({
  name,
  operations: operations.filter((entry) => entry.group === name),
}));

if (operations.length !== 115) {
  throw new Error(`Expected 115 southbound operations, found ${operations.length}.`);
}

const operationCount = operations.length;
const queryOperations = operations.filter((entry) => entry.kind === "query");
const commandOperations = operations.filter((entry) => entry.kind === "command");
const aliasEntries = operations.flatMap((entry) =>
  (entry.aliases ?? []).map((alias) => ({
    alias,
    operation: entry.operation,
  })),
);

function quote(value) {
  return JSON.stringify(value);
}

function toRustKind(kind) {
  return kind === "query" ? "ClientOperationKind::Query" : "ClientOperationKind::Command";
}

function toRustTransportVariant(transportVariant) {
  switch (transportVariant) {
    case "legacy":
      return "ClientTransportVariant::Legacy";
    case "telemetry":
      return "ClientTransportVariant::Telemetry";
    case "mission_sync":
      return "ClientTransportVariant::MissionSync";
    case "checklist":
      return "ClientTransportVariant::Checklist";
    default:
      throw new Error(`Unknown transport variant: ${transportVariant}`);
  }
}

